static void filter(const double b[5], const double a[5], const double x[174], const double zi[4], double y[174])
{
  int k;
  int naxpy;
  int j;
  double as;
  for (k = 0; k < 4; k++)
  {
    y[k] = zi[k];
  }

  memset(&y[4], 0, 170U * sizeof(double));
  for (k = 0; k < 174; k++)
  {
    naxpy = 174 - k;
    if (!(naxpy < 5))
    {
      naxpy = 5;
    }

    for (j = 0; j + 1 <= naxpy; j++)
    {
      y[k + j] += x[k] * b[j];
    }

    naxpy = 173 - k;
    if (!(naxpy < 4))
    {
      naxpy = 4;
    }

    as = -y[k];
    for (j = 1; j <= naxpy; j++)
    {
      y[k + j] += as * a[j];
    }
  }
}

